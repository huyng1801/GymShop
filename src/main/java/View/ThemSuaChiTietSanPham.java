    private void jBChonAnhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBChonAnhActionPerformed
        // TODO add your handling code here:
         JFileChooser fileChooser = new JFileChooser();
                fileChooser.setCurrentDirectory(new File("D:\\Project\\Java\\GymShop\\src\\main\\java\\Images"));
                // Chỉ cho phép chọn các file ảnh (định dạng: jpg, png, gif)
                FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("Image files", "jpg", "png", "gif");
                fileChooser.setFileFilter(imageFilter);
                int result = fileChooser.showOpenDialog(this);
                if (result == JFileChooser.APPROVE_OPTION) {
                    // Đường dẫn tới file ảnh đã chọn
                    File selectedFile = fileChooser.getSelectedFile();
                    String imagePath = fileChooser.getSelectedFile().getAbsolutePath();
                    // Đọc dữ liệu từ file
Path path = Paths.get(imagePath);
byte[] imageData = null;
try {
    imageData = Files.readAllBytes(path);
    this.image = imageData;
} catch (IOException e) {
    e.printStackTrace();
}
                    // Tạo ImageIcon từ file ảnh
                    ImageIcon imageIcon = new ImageIcon(imagePath);
                    // Scale ảnh
                    Image scaledImage = imageIcon.getImage().getScaledInstance(jLAnh.getWidth(), jLAnh.getHeight(), Image.SCALE_SMOOTH);
                    // Tạo ImageIcon từ ảnh đã được scale
                    ImageIcon scaledImageIcon = new ImageIcon(scaledImage);
                    // Gán ImageIcon vào JLabel
                    this.jLAnh.setIcon(scaledImageIcon);
                    // Thực hiện xử lý file ảnh tại đây
                }
          
    }//GEN-LAST:event_jBChonAnhActionPerformed

