    private void jBLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBLuuActionPerformed
        // TODO add your handling code here:
        spctl = new SanPhamController();
        if(this.status == 0){
        String maSanPham = this.jTFMaSanPham.getText();
        String tenSanPham = this.jTFTenSanPham.getText();
        double gia = Double.parseDouble(this.jTFGia.getText());
        
        String tenDanhMuc = this.jCBDanhMucSanPham.getItemAt(this.jCBDanhMucSanPham.getSelectedIndex());
        int soLuong = this.jSFSoLuong.getValue();
        
        if(image != null){
            SanPhamModel sp = new SanPhamModel(maSanPham, tenSanPham, gia, soLuong,tenDanhMuc, image);
        spctl.themSanPham(sp);
         JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
         this.dispose();
         gdsp.hienThiSanPham();
        }
        else{
        JOptionPane.showMessageDialog(this, "Vui lòng chọn ảnh!");
        }
        }
        else{
        String maSanPham = this.jTFMaSanPham.getText();
        String tenSanPham = this.jTFTenSanPham.getText();
        double gia = Double.parseDouble(this.jTFGia.getText());
        
        String tenDanhMuc = this.jCBDanhMucSanPham.getItemAt(this.jCBDanhMucSanPham.getSelectedIndex());
        int soLuong = this.jSFSoLuong.getValue();
        
        if(image != null){
            SanPhamModel sp = new SanPhamModel(maSanPham, tenSanPham, gia, soLuong,tenDanhMuc, image);
        spctl.suaSanPham(sp);
         JOptionPane.showMessageDialog(this, "Sửa sản phẩm thành công!");
         gdsp.hienThiSanPham();
         this.dispose();
        }
        else{
        JOptionPane.showMessageDialog(this, "Vui lòng chọn ảnh!");
        }
        }
    }//GEN-LAST:event_jBLuuActionPerformed

